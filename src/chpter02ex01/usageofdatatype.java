package chpter02ex01;

public class usageofdatatype {
	public static void main(String[] args) {
		
		//# 변수 선언과 동시에 값 대입
		int a = 10 ;
		System.out.println(a);
		
		
		//# 변수 선언후 값을 대입
       int b ;   //변수 선언, java는 비어있는 상태가 된다.
                // 참고, c언어는 변수 선언만 했을때 가비지(쓰레기) 들어감.
       b= 30;   // 값 할당
       System.out.println(b);
       
       
		
	}
	
	
	

}
